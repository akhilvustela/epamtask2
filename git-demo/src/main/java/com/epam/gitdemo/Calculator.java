package com.epam.gitdemo;

public class Calculator {
	public int add(int a,int b) {
		return a+b;
	}
	public int subract(int a,int b) {
		return a-b;
	}
	public int multiply(int a,int b) {
		return a*b;
	}
	public int divide(int a,int b) {
		return a/b;
	}
	public int modulus(int a,int b) {
		return a%b;
	}
}


